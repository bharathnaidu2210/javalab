import java.util.*;


 class PrimitiveDatatypes
 
 {static  int a;
  static  double b;
 static   float c;
 static byte d;
  static  char e;
   static boolean f;
   static short h;
   public static void main(String args[])
   { 
    PrimitiveDatatypes obj=new PrimitiveDatatypes();
    System.out.println("int default value = "+obj.a);
    System.out.println("byte default value = "+obj.d);
    System.out.println("char default value = "+obj.e);
    System.out.println("float default value = "+obj.c);
    System.out.println("double default value = "+obj.b);
    System.out.println("boolean default value = "+obj.f);
    System.out.println("short default value = "+obj.h);
    
   }
 }

